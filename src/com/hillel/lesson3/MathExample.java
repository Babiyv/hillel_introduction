package com.hillel.lesson3;

public class MathExample {
    public static void main(String[] args) {

        int minDigit = Math.min(100, 45);
        int maxDigit = Math.max(4, 19);

        System.out.println("min digit value is: " + minDigit);
        System.out.println("max digit value is: " + maxDigit);
        System.out.println(minDigit + "- is min digit value"); // в таком формате вывода, когда вначале число, а потом текст, усложняется логика программы



    }
}
